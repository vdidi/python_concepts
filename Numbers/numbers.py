from math import *

# Aritimética básica

print(3 * 4 + 5)

print(3 * (4 + 5))

# Resto
print(10 % 3)

# Transformando um número numa string
print(str(2) + " é um número")

# Valor absoluto
print(abs(-22))

# Potência
print(pow(5, 3))

# Valor máximo entre dois números
print(max(5, 3))

# Valor mínimo entre dois números
print(min(5, 2))

# Arredondando por baixo
print(floor(5.8))

# Arredondando por cima 4.2
print(ceil(4.2))

# Arredondando 3.7
print(round(3.7))

# Raiz quadrada de 36
print(sqrt(36))